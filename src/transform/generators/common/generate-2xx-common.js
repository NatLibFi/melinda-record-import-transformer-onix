import {getFirstValueInContext, hasAttribute} from '../../../utils/data-utils.js';
import {getImprintNames, getPublisherNames, getPublishingDates, isSimplifiedLanguageEdition} from '../../product-utils.js';
import {getContributors, getRecordMainLangs, getTitle, translateLanguageCode} from '../../record-utils.js';
import {generate130Common} from './generate-1xx-common.js';


/**
 * Generates 240 field for print and electronical records if title with TitleType value of 03 is found.
 * Field is not generated for simplified language editions or if main author is not defined.
 * @param {import('../../../types.js').OnixConversionConfiguration} onixConversionConfiguration - configuration for ONIX->MARC21 conversion
 * @param {import('../../../types.js').ValueInterface} valueInterface ValueInterface containing getValue/getValues functions
 * @returns {import('../../../types.js').DataField[]} Array containing field 240
 */
export function generate240Common(onixConversionConfiguration, valueInterface) {
  /*
  Onix Codelists: List 15: TitleType
  https://ns.editeur.org/onix/en/15

  03 | Title in original language
  */

  const {preventUniformTitleGeneration, languageSanityCheck} = onixConversionConfiguration;
  const {mainAuthor} = getContributors(valueInterface);

  if (preventUniformTitleGeneration || mainAuthor === null) {
    return [];
  }

  const unifiedTitles = valueInterface.getValues('DescriptiveDetail', 'TitleDetail').filter(title => {
    const requiredProperties = ['TitleType', 'TitleElement'];
    const hasRequiredProperties = requiredProperties.every(property => hasAttribute(title, property));

    if (!hasRequiredProperties) {
      return false;
    }

    const hasRequiredTitleType = getFirstValueInContext(title, 'TitleType') === '03';

    if (!hasRequiredTitleType) {
      return false;
    }

    const titleText = getFirstValueInContext(title, 'TitleElement', 'TitleText');
    return typeof titleText === 'string' && titleText.length > 0;
  });

  if (unifiedTitles.length === 0) {
    return [];
  }

  const [unifiedTitle] = unifiedTitles;
  const unifiedTitleText = getFirstValueInContext(unifiedTitle, 'TitleElement', 'TitleText');

  // Generate language subfield only if exactly one language is observed
  const languages = getRecordMainLangs(valueInterface, languageSanityCheck);
  const translatedLanguage = languages.length === 1 ? translateLanguageCode(languages[0]) : null;
  const languageSubfield = translatedLanguage ? [{code: 'l', value: `${translatedLanguage}`}] : [];

  // For SMP editions, an additional $s is created
  const subfieldS = isSimplifiedLanguageEdition(valueInterface) ? [{code: 's', value: '(selkokieli)'}] : [];

  // Note: ind2 is set by validator

  return [
    {
      tag: '240',
      ind1: '1',
      subfields: [
        {code: 'a', value: `${unifiedTitleText}.`},
        ...languageSubfield,
        ...subfieldS
      ]
    }
  ];
}

/**
 * Generates 245 field for print and electronical records if Title with TitleType value of 00 or 01 is found.
 * @param {import('../../../types.js').OnixConversionConfiguration} onixConversionConfiguration - configuration for ONIX->MARC21 conversion
 * @param {import('../../../types.js').ValueInterface} valueInterface ValueInterface containing getValue/getValues functions
 * @returns {import('../../../types.js').DataField[]} Array containing field 245
 * @throws Error in case valid f245 cannot be created as this would create invalid record
*/

export function generate245Common(onixConversionConfiguration, valueInterface) {
  const {mainAuthor} = getContributors(valueInterface);

  const hasMainAuthor = mainAuthor !== null;
  const hasf130 = generate130Common(onixConversionConfiguration, valueInterface).length > 0;

  const ind1 = hasMainAuthor || hasf130 ? '1' : '0';
  // ind2 is generated by validator

  const {title, subtitle} = getTitle(valueInterface, true);

  // Disallow generating records without f245
  if (!title) {
    throw new Error('Could not generate field 245 due to missing title information');
  }

  if (subtitle) {
    return [
      {
        tag: '245',
        ind1,
        subfields: [
          {code: 'a', value: `${title} :`},
          {code: 'b', value: subtitle}
        ]
      }
    ];
  }

  return [
    {
      tag: '245',
      ind1,
      subfields: [
        {code: 'a', value: `${title}.`}
      ]
    }
  ];
}

/**
 * Generates 246 field for print and electronical records if title with TitleType value of 11, 12, or 13 is found.
 * @param {import('../../../types.js').OnixConversionConfiguration} _onixConversionConfiguration - configuration for ONIX->MARC21 conversion
 * @param {import('../../../types.js').ValueInterface} valueInterface ValueInterface containing getValue/getValues functions
 * @returns {import('../../../types.js').DataField[]} Array containing field 246
*/
export function generate246Common(_onixConversionConfiguration, valueInterface) {
  /*
  Onix Codelists: List 15: Title type code
  https://ns.editeur.org/onix/en/21

  11 | Alternative title on cover | An alternative title that appears on the cover of a book
  12 | Alternative title on back | An alternative title that appears on the back of a book
  13 | Expanded title | An expanded form of the title, eg the title of a school text book with grade and type and other details added to make the title meaningful, where otherwise it would comprise only the curriculum subject.
  */

  const validTitleTypes = ['11', '12', '13'];
  const titleDetails = valueInterface.getValues('DescriptiveDetail', 'TitleDetail').filter(titleDetail => {
    const hasValidTitleType = validTitleTypes.includes(getFirstValueInContext(titleDetail, 'TitleType'));
    const hasTitleElement = hasAttribute(titleDetail, 'TitleElement');

    if (!hasValidTitleType | !hasTitleElement) {
      return false;
    }

    const titleElement = getFirstValueInContext(titleDetail, 'TitleElement');
    const hasTitleText = hasAttribute(titleElement, 'TitleText');

    return hasTitleText;
  });

  return titleDetails.map(titleDetail => {
    const titleType = getFirstValueInContext(titleDetail, 'TitleType');
    const titleElement = getFirstValueInContext(titleDetail, 'TitleElement');
    const titleText = getFirstValueInContext(titleElement, 'TitleText');

    let ind1;
    let ind2;

    if (titleType === '11') {
      ind1 = '1';
      ind2 = '4';
    }

    if (titleType === '12') {
      ind1 = '1';
      ind2 = '8';
    }

    if (titleType === '13') {
      ind1 = '1';
      ind2 = ' '; // Note: unusual empty definition of ind2 needed here for code readability. Normally empty indicators are handled by MarcRecord constructor.
    }

    return {
      tag: '246',
      ind1,
      ind2,
      subfields: [
        {code: 'a', value: titleText}
      ]
    };
  });
}

/**
 * Generates 250 field for print and electronical records from EditionNumber information
 * @param {import('../../../types.js').OnixConversionConfiguration} _onixConversionConfiguration - configuration for ONIX->MARC21 conversion
 * @param {import('../../../types.js').ValueInterface} valueInterface ValueInterface containing getValue/getValues functions
 * @returns {import('../../../types.js').DataField[]} Array containing field 250
*/
export function generate250Common(_onixConversionConfiguration, valueInterface) {
  const editionNr = valueInterface.getValue('DescriptiveDetail', 'EditionNumber');

  if (!editionNr) {
    return [];
  }

  return [
    {
      tag: '250',
      subfields: [{code: 'a', value: `${editionNr}. painos.`}]
    }
  ];
}

/**
 * Generates 263 field for print and electronical records. Field is generated only for early notification or advance notification entries.
 * Publishing date or last reprint date definition is mandatory for field generation.
 * @param {import('../../../types.js').OnixConversionConfiguration} _onixConversionConfiguration - configuration for ONIX->MARC21 conversion
 * @param {import('../../../types.js').ValueInterface} valueInterface ValueInterface containing getValue/getValues functions
 * @returns {import('../../../types.js').DataField[]} Array containing field 263
 */
export function generate263Common(_onixConversionConfiguration, valueInterface) {
  /*
  Onix Code Lists: List 1: Notification or update type
  https://ns.editeur.org/onix/en/1

  01 | Early notification | Use for a complete record issued earlier than approximately six months before publication
  02 | Advance notification (confirmed) | Use for a complete record issued to confirm advance information approximately six months before publication; or for a complete record issued after that date and before information has been confirmed from the book-in-hand


  Onix Code Lists:  List 163: Publishing date role
  https://ns.editeur.org/onix/en/163

  01 | Publication date | Nominal date of publication. This date is primarily used for planning, promotion and other business process purposes, and is not necessarily the first date for retail sales or fulfillment of pre-orders. In the absence of a sales embargo date, retail sales and pre-order fulfillment may begin as soon as stock is available to the retailer
  12 | Last reprint date | Date when a product was last reprinted
  */

  const notificationType = valueInterface.getValue('NotificationType');
  const publishingDates = valueInterface.getValues('PublishingDetail', 'PublishingDate');

  const acceptedNotificationTypes = ['01', '02'];
  const acceptedPublishingDateRoles = ['01', '12'];

  const notificationTypeIsSupported = acceptedNotificationTypes.includes(notificationType);

  const acceptedPublishingDates = publishingDates
    .map(parsePublishingDate)
    .filter(publishingDate => acceptedPublishingDateRoles.includes(publishingDate.role) && publishingDate.value);
  const hasAcceptedPublishingDate = acceptedPublishingDates.length > 0;

  if (!notificationTypeIsSupported || !hasAcceptedPublishingDate) {
    return [];
  }

  // Prioritize using last reprint date if it is available. Otherwise use first value of accepted publishing dates.
  const publishingDate = acceptedPublishingDates.find(publishingDate => publishingDate.role === '12') || acceptedPublishingDates[0];

  return [
    {
      tag: '263',
      subfields: [{code: 'a', value: publishingDate.value}]
    }
  ];


  /**
   * Helper function for getting publishing date value out of publishing date information
   * @param {*} publishingDateInformation - entry of return value from getValues('PublishingDetail', 'PublishingDate')
   * @returns {{date: string|null, role: string|null}}
   */
  function parsePublishingDate(publishingDateInformation) {
    const date = getFirstValueInContext(publishingDateInformation, 'Date');
    const role = getFirstValueInContext(publishingDateInformation, 'PublishingDateRole');

    const dateValue = typeof date === 'object' && date._ ? date._ : typeof date === 'string' ? date : null;
    return {role, value: dateValue};
  }
}

/**
 * Generates 264 field for print and electronical records. Prioritizes using imprint information over publisher information.
 * @param {import('../../../types.js').OnixConversionConfiguration} _onixConversionConfiguration - configuration for ONIX->MARC21 conversion
 * @param {import('../../../types.js').ValueInterface} valueInterface ValueInterface containing getValue/getValues functions
 * @returns {import('../../../types.js').DataField[]} Array containing field 264
 */
export function generate264Common(_onixConversionConfiguration, valueInterface) {
  const [earliestPublishingDate = {year: null}] = getPublishingDates(valueInterface);

  const imprintNames = getImprintNames(valueInterface);
  const publisherNames = getPublisherNames(valueInterface);

  // Prioritize imprint information over publisher information
  const subfieldBValues = imprintNames.length > 0 ? imprintNames : publisherNames;

  // Begin subfield generation by generating $b for each value
  const subfields = subfieldBValues.map((subfieldBValue, idx) => {
    const isLastField = idx === subfieldBValues.length - 1;

    const hasMultipleB = subfieldBValues.length > 0;
    const hasSubfieldC = earliestPublishingDate.year !== null;

    let punctuation = '';

    if (hasMultipleB) {
      punctuation = ' :';
    }

    // Note: this will override previous conditional on purpose
    // Last $b should always have either ',' or '' as punctuation depending on $c
    if (isLastField) {
      punctuation = hasSubfieldC ? ',' : '';
    }

    return {
      code: 'b',
      value: `${subfieldBValue}${punctuation}`
    };
  });

  // Do not return field if it does not contain $b
  if (subfields.length === 0) {
    return [];
  }

  if (earliestPublishingDate.year) {
    subfields.push({code: 'c', value: `${earliestPublishingDate.year}.`});
  }

  return [{
    tag: '264',
    ind2: '1',
    subfields
  }];
}
